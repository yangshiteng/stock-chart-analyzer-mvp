export function getLanguage(settingValue) {
  return settingValue === "zh" ? "zh" : DEFAULT_LANGUAGE;
}

export function t(language, key, vars = {}) {
  const locale = TRANSLATIONS[language] || TRANSLATIONS[DEFAULT_LANGUAGE];
  const fallback = TRANSLATIONS[DEFAULT_LANGUAGE];
  const template = locale[key] || fallback[key] || key;

  return template.replace(/\{(\w+)\}/g, (_, name) => `${vars[name] ?? ""}`);
}
